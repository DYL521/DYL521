const langToggle = document.querySelector(".lang-toggle");
const translatableNodes = document.querySelectorAll("[data-i18n]");
const revealNodes = document.querySelectorAll(".reveal");
const hero = document.querySelector(".hero");
const heroCopy = document.querySelector(".hero-copy");
const heroDevice = document.querySelector(".hero-device");
const heroGrid = document.querySelector(".hero-grid");
const heroRings = document.querySelectorAll(".hero-ring");
const screenOrbits = document.querySelectorAll(".screen-orbit");
const storyCards = document.querySelector(".story-cards");
const projectGrid = document.querySelector(".project-grid");
const spotlightGrid = document.querySelector(".spotlight-grid");

function applyLanguage(language) {
  const dictionary = translations[language];
  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });
  langToggle.textContent = language === "en" ? "中文" : "English";
}

applyLanguage("en");

langToggle.addEventListener("click", () => {
  const nextLanguage = document.documentElement.lang === "en" ? "zh" : "en";
  applyLanguage(nextLanguage);
});

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.18 }
);

revealNodes.forEach((node) => observer.observe(node));

function clamp(value, min, max) {
  return Math.min(Math.max(value, min), max);
}

function segmentProgress(progress, start, end) {
  return clamp((progress - start) / (end - start), 0, 1);
}

function updateScrollEffects() {
  const offset = window.scrollY * 0.12;
  document.querySelectorAll(".aurora").forEach((node, index) => {
    const direction = index === 0 ? -1 : 1;
    node.style.transform = `translate3d(${direction * offset}px, ${offset * 0.6}px, 0)`;
  });

  const stage = document.querySelector(".cinema-stage");
  if (stage) {
    stage.style.transform = `translateY(${Math.min(offset * 0.16, 22)}px)`;
  }

  if (hero) {
    const heroRect = hero.getBoundingClientRect();
    const heroProgress = clamp((window.innerHeight - heroRect.top) / (window.innerHeight + heroRect.height), 0, 1);
    const heroLift = heroProgress * 42;
    const deviceLift = heroProgress * 58;

    if (heroCopy) {
      heroCopy.style.transform = `translate3d(0, ${-heroLift}px, 0)`;
    }

    if (heroDevice) {
      heroDevice.style.transform = `translate3d(0, ${-deviceLift}px, 0) scale(${1 + heroProgress * 0.035})`;
    }

    if (heroGrid) {
      heroGrid.style.transform = `translate3d(0, ${offset * 0.22}px, 0)`;
      heroGrid.style.opacity = `${0.18 + heroProgress * 0.2}`;
    }

    heroRings.forEach((ring, index) => {
      const direction = index === 0 ? 1 : -1;
      ring.style.transform = `translate3d(${direction * heroProgress * 18}px, ${heroProgress * 24}px, 0) scale(${1 + heroProgress * 0.04})`;
    });

    screenOrbits.forEach((orbit, index) => {
      const direction = index === 0 ? -1 : 1;
      orbit.style.transform = `translate3d(${direction * heroProgress * 16}px, ${heroProgress * 20}px, 0) rotate(${direction * heroProgress * 10}deg)`;
    });
  }

  if (storyCards) {
    const rect = storyCards.getBoundingClientRect();
    const progress = clamp((window.innerHeight - rect.top) / (window.innerHeight + rect.height), 0, 1);
    storyCards.style.transform = `translate3d(0, ${28 - progress * 28}px, 0)`;
  }

  if (projectGrid) {
    const rect = projectGrid.getBoundingClientRect();
    const progress = clamp((window.innerHeight - rect.top) / (window.innerHeight + rect.height), 0, 1);
    projectGrid.style.transform = `translate3d(0, ${34 - progress * 34}px, 0)`;
  }

  if (spotlightGrid) {
    const rect = spotlightGrid.getBoundingClientRect();
    const progress = clamp((window.innerHeight - rect.top) / (window.innerHeight + rect.height), 0, 1);
    spotlightGrid.style.transform = `translate3d(0, ${30 - progress * 30}px, 0)`;
  }

}

window.addEventListener("scroll", updateScrollEffects);
updateScrollEffects();
